function toggleBurger() {
	var x = document.getElementById("main-nav");
	
	if (x.className === "topnav") 
    	x.className += " responsive";
  	else 
    	x.className = "topnav";  
} 

$(document).ready(function() {
	// Catalog lightbox images
	$(".lightbox").lightBox({
		overlayBgColor: '#FFF',
		overlayOpacity: 0.6,
		containerResizeSpeed: 350,
    });

	// Top nav links
	$('.top-anchor').click(function(e) {
		e.preventDefault(); 
		var scrollId = this.id.substr(0, this.id.indexOf('_'));  
		$('html, body').animate({
			scrollTop: $('#' + scrollId).offset().top 
		}, 500);
	});

	// Product detail info
	$('.more').click(function(e) {
		e.preventDefault();
		$('.popup').fadeOut(700);
		var prodId = this.id.substr(0, this.id.indexOf('_'));
		$('#product_' + prodId).fadeIn(700);
	});

	$('.closeme').click(function() {
		$('.popup').fadeOut(700);
	});

	// Toggle delivery details
	$('.hide-details').click(function() {
		$(this).toggleClass('hidden');

		if($(this).hasClass('hidden')) {
			$('.delivery-section-2').fadeOut(700);
			$(this).text('ПОКАЗАТЬ ПОДРОБНОСТИ');
		}
		else {
			$('.delivery-section-2').fadeIn(700);
			$(this).text('СКРЫТЬ ПОДРОБНОСТИ'); 			
		}
	});

    // Callback button downscroll
	$('.callback, .price-order').click(function() {
		$('html, body').animate({
			scrollTop: $('.callback-form').offset().top 
